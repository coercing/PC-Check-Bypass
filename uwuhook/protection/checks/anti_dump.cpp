#include <Windows.h>
#include <winnt.h>
#include "../../main/includes.hpp"

void anti_dump()
{
#ifdef _WIN64
    const auto peb = reinterpret_cast<PPEB>(__readgsqword(0x60));
#else
    const auto peb = reinterpret_cast<PPEB>(__readfsdword(0x30));
#endif

    const auto in_load_order_module_list = reinterpret_cast<PLIST_ENTRY>(peb->Ldr->Reserved2[1]);
    const auto table_entry = CONTAINING_RECORD(in_load_order_module_list, LDR_DATA_TABLE_ENTRY, Reserved1[0]);
    auto* p_size_of_image = reinterpret_cast<PULONG>(&table_entry->Reserved3[1]);
    *p_size_of_image = static_cast<ULONG>(reinterpret_cast<INT_PTR>(table_entry->DllBase) + 0x100000);
}